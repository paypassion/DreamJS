// 할당연산자 (Assignment operators)

let a = 1;
a = a + 2;
console.log(a);

a += 2; // a = a + 2; 축약버전
console.log(a);

a -= 2; // a = a - 2; 축약버전
console.log(a);

a *=2; // a = a * 2; 축약버전
console.log(a);

a /=2; // a = a / 2; 축약버전
console.log(a);

a %=2; // a = a를 2로 나눈 나머지; 축약버전
console.log(a);

a=3
a **=2; // a = a의 제곱; 축약버전
console.log(a);

